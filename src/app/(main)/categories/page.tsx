"use client"

import { useState, useEffect } from 'react'
import Image from "next/image"
import { Card, CardContent } from "@/components/ui/card"
import { Button } from "@/components/ui/button"
import ProductPreviewModal from "@/components/ProductPreviewModal"
import { Package, Tag } from 'lucide-react'
import { CategoryProduct, Category } from '@/types/shared'

export default function CategoriesPage() {
  const [categories, setCategories] = useState<Category[]>([])
  const [loading, setLoading] = useState(true)
  const [selectedProduct, setSelectedProduct] = useState<CategoryProduct | null>(null)
  const [isModalOpen, setIsModalOpen] = useState(false)
  const [selectedCategory, setSelectedCategory] = useState<string>('all')

  useEffect(() => {
    fetchCategories()
  }, [])

  const fetchCategories = async () => {
    try {
      const response = await fetch('/api/categories')
      if (response.ok) {
        const data = await response.json()
        setCategories(data)
      } else {
        console.error('Failed to fetch categories')
      }
    } catch (error) {
      console.error('Error fetching categories:', error)
    } finally {
      setLoading(false)
    }
  }

  const handlePreviewClick = (product: CategoryProduct) => {
    setSelectedProduct(product)
    setIsModalOpen(true)
  }

  const handleCloseModal = () => {
    setIsModalOpen(false)
    setSelectedProduct(null)
  }

  const filteredCategories = selectedCategory === 'all' 
    ? categories 
    : categories.filter(cat => cat.slug === selectedCategory)

  const allProducts = categories.flatMap(cat => cat.products)

  if (loading) {
    return (
      <div className="min-h-screen flex items-center justify-center">
        <div className="animate-spin rounded-full h-32 w-32 border-b-2 border-[var(--primary-light)]"></div>
      </div>
    )
  }

  return (
    <div className="min-h-screen bg-[var(--accent-cream)]">
      {/* Hero Section */}
      <div className="bg-gradient-to-r from-[var(--primary-dark)] to-[var(--primary-medium)] py-16">
        <div className="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8 text-center">
          <h1 className="text-4xl md:text-5xl font-bold text-[var(--accent-cream)] mb-4">
            Our Product Categories
          </h1>
          <p className="text-xl text-[var(--primary-light)] max-w-3xl mx-auto">
            Explore our comprehensive range of quality products across different categories
          </p>
        </div>
      </div>

      {/* Category Filter */}
      <div className="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8 py-8">
        <div className="flex flex-wrap justify-center gap-4 mb-8">
          <button
            onClick={() => setSelectedCategory('all')}
            className={`px-6 py-3 rounded-lg font-medium transition-colors ${
              selectedCategory === 'all'
                ? 'bg-[var(--primary-light)] text-white'
                : 'bg-white text-[var(--primary-dark)] border border-[var(--primary-light)] hover:bg-[var(--primary-light)]'
            }`}
          >
            All Categories ({allProducts.length} products)
          </button>
          {categories.map((category) => (
            <button
              key={category.id}
              onClick={() => setSelectedCategory(category.slug)}
              className={`px-6 py-3 rounded-lg font-medium transition-colors ${
                selectedCategory === category.slug
                  ? 'bg-[var(--primary-light)] text-white'
                  : 'bg-white text-[var(--primary-dark)] border border-[var(--primary-light)] hover:bg-[var(--primary-light)]'
              }`}
            >
              {category.name} ({category.products.length})
            </button>
          ))}
        </div>

        {/* Categories Display */}
        <div className="space-y-16">
          {filteredCategories.map((category) => (
            <div key={category.id} className="bg-white rounded-2xl shadow-lg overflow-hidden">
              {/* Category Header */}
              <div className="bg-gradient-to-r from-[var(--primary-medium)] to-[var(--primary-light)] p-8">
                <div className="flex items-center justify-between">
                  <div>
                    <h2 className="text-3xl font-bold text-white mb-2 flex items-center gap-3">
                      <Tag className="w-8 h-8" />
                      {category.name}
                    </h2>
                    {category.description && (
                      <p className="text-[var(--primary-light)] text-lg">{category.description}</p>
                    )}
                  </div>
                  <div className="text-right">
                    <div className="flex items-center gap-2 text-white">
                      <Package className="w-5 h-5" />
                      <span className="text-2xl font-bold">{category.products.length}</span>
                    </div>
                    <p className="text-[var(--primary-light)] text-sm">Products</p>
                  </div>
                </div>
              </div>

              {/* Products Grid */}
              {category.products.length > 0 ? (
                <div className="p-8">
                  <div className="grid grid-cols-1 sm:grid-cols-2 lg:grid-cols-3 xl:grid-cols-4 gap-6">
                    {category.products.map((product) => (
                      <Card key={product.id} className="group relative overflow-hidden border-[var(--primary-light)] hover:shadow-xl transition-all duration-300 hover:-translate-y-1">
                        <CardContent className="p-0 h-full flex flex-col">
                          {/* Product Image */}
                          <div className="relative bg-[var(--accent-cream)] p-6 h-64 flex items-center justify-center">
                            <Image
                              src={product.image || "https://via.placeholder.com/300x300?text=Product+Image"}
                              alt={product.title}
                              fill
                              className="object-contain group-hover:scale-105 transition-transform duration-300"
                            />
                          </div>

                          {/* Product Info */}
                          <div className="p-4 flex flex-col flex-1">
                            <h3 className="text-sm font-medium text-[var(--primary-dark)] mb-2 line-clamp-2 min-h-[2.5rem]">
                              {product.title}
                            </h3>
                            
                            {/* Price */}
                            <div className="mb-4 flex-1">
                              {product.status === "SOLD_OUT" ? (
                                <span className="text-[var(--primary-light)] font-semibold text-sm">
                                  Sold Out
                                </span>
                              ) : (
                                <div className="flex items-center gap-2">
                                  <span className="text-lg font-bold text-[var(--primary-dark)]">
                                    Rs. {product.salePrice}
                                  </span>
                                  {product.originalPrice && (
                                    <span className="text-sm text-[var(--primary-medium)] line-through">
                                      Rs. {product.originalPrice}
                                    </span>
                                  )}
                                </div>
                              )}
                            </div>

                            {/* Action Button */}
                            <Button 
                              className={`w-full mt-auto ${
                                product.status === "SOLD_OUT"
                                  ? "bg-[var(--primary-medium)] opacity-50 cursor-not-allowed"
                                  : "bg-[var(--primary-light)] hover:bg-[var(--primary-dark)]"
                              }`}
                              disabled={product.status === "SOLD_OUT"}
                              onClick={() => product.status === "AVAILABLE" && handlePreviewClick(product)}
                            >
                              {product.status === "SOLD_OUT" ? "SOLD OUT" : "PREVIEW"}
                            </Button>
                          </div>
                        </CardContent>
                      </Card>
                    ))}
                  </div>
                </div>
              ) : (
                <div className="p-16 text-center text-[var(--primary-medium)]">
                  <Package className="w-16 h-16 mx-auto mb-4 opacity-50" />
                  <p className="text-lg">No products available in this category yet.</p>
                </div>
              )}
            </div>
          ))}
        </div>

        {/* Empty State */}
        {categories.length === 0 && (
          <div className="text-center py-16">
            <Tag className="w-24 h-24 mx-auto mb-6 text-[var(--primary-medium)] opacity-50" />
            <h2 className="text-2xl font-bold text-[var(--primary-dark)] mb-4">
              No Categories Available
            </h2>
            <p className="text-[var(--primary-medium)] text-lg">
              Categories will be displayed here once they are added.
            </p>
          </div>
        )}
      </div>

      {/* Product Preview Modal */}
      <ProductPreviewModal
        isOpen={isModalOpen}
        onClose={handleCloseModal}
        product={selectedProduct}
      />
    </div>
  )
}